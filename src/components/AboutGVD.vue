          Violence Against Women Database to the Gender Violence Database in
          order to be as inclusive as possible and reflect the spectrum of
          subject matter addressed on the site. Lastly, they located a
          communication design undergraduate to develop a logo for the site and
          an overall design. In May of 2014, the team launched the Gender
          Violence Database to the public.
